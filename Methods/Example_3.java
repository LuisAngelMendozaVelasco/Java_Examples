// Method with parameters //
////////////////////////////

public class Example_3 {
    static void myMethod(String fname) {
        System.out.println(fname + " Refsnes");
    }
    
    public static void main(String[] args) {
        myMethod("Liam");
        myMethod("Jenny");
        myMethod("Anja");
    }
}

/*
Output:

Liam Refsnes
Jenny Refsnes
Anja Refsnes
*/