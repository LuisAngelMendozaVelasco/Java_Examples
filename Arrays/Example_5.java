///////////////////////////
// Loop through an array //
///////////////////////////

public class Example_5 {
    public static void main(String[] args) {
        String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};

        for(int i = 0; i < cars.length; i++) {
            System.out.println(cars[i]);
        }
    }
}

/*
Output:

Volvo
BMW
Ford
Mazda
*/